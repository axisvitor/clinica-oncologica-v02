"""
Report schemas for API validation.
"""
from datetime import date, datetime
from typing import List, Optional, Any
from uuid import UUID

from pydantic import BaseModel, Field, field_validator, ConfigDict


class ReportSectionSchema(BaseModel):
    """Report section schema."""
    title: str = Field(..., description="Section title")
    content: str = Field(..., description="Section content")
    subsections: List['ReportSectionSchema'] = Field(default_factory=list, description="Nested subsections")


class ReportGenerationRequest(BaseModel):
    """Request schema for report generation."""
    patient_id: UUID = Field(..., description="Patient ID")
    period_start: date = Field(..., description="Report period start date")
    period_end: date = Field(..., description="Report period end date")
    include_sections: List[str] = Field(
        default_factory=lambda: ["summary", "treatment", "symptoms", "adherence", "recommendations"],
        description="Sections to include in report"
    )
    format: str = Field(default="pdf", description="Report format (pdf, json)")

    @field_validator('period_end')
    @classmethod
    def validate_period(cls, v, info):
        if info.data.get('period_start') and v < info.data['period_start']:
            raise ValueError('period_end must be after period_start')
        return v


class ReportPreviewResponse(BaseModel):
    """Report preview response schema."""
    patient_name: str = Field(..., description="Patient name")
    patient_id: UUID = Field(..., description="Patient ID")
    doctor_name: str = Field(..., description="Doctor name")
    report_period: str = Field(..., description="Report period description")
    generated_date: datetime = Field(..., description="Report generation date")
    
    # Report sections
    executive_summary: str = Field(..., description="Executive summary")
    treatment_progress: ReportSectionSchema = Field(..., description="Treatment progress section")
    symptoms_analysis: ReportSectionSchema = Field(..., description="Symptoms analysis section")
    medication_adherence: ReportSectionSchema = Field(..., description="Medication adherence section")
    recommendations: ReportSectionSchema = Field(..., description="Recommendations section")
    
    # Data summaries
    quiz_responses_count: int = Field(..., description="Number of quiz responses")
    message_statistics: dict[str, Any] = Field(..., description="Message statistics")
    alert_count: int = Field(..., description="Number of alerts")


class MedicalReportResponse(BaseModel):
    """Medical report response schema."""
    id: UUID = Field(..., description="Report ID")
    patient_id: UUID = Field(..., description="Patient ID")
    generated_by: UUID = Field(..., description="Generated by user ID")
    period_start: date = Field(..., description="Report period start")
    period_end: date = Field(..., description="Report period end")
    summary: Optional[str] = Field(None, description="AI-generated summary")
    insights: dict[str, Any] = Field(default_factory=dict, description="Structured insights")
    charts_data: dict[str, Any] = Field(default_factory=dict, description="Chart data")
    alerts: dict[str, Any] = Field(default_factory=dict, description="Alert data")
    created_at: datetime = Field(..., description="Creation timestamp")
    updated_at: datetime = Field(..., description="Last update timestamp")

    model_config = ConfigDict(from_attributes=True)


class ReportListResponse(BaseModel):
    """Report list response schema."""
    reports: List[MedicalReportResponse] = Field(..., description="List of reports")
    total: int = Field(..., description="Total number of reports")
    page: int = Field(..., description="Current page")
    size: int = Field(..., description="Page size")
    pages: int = Field(..., description="Total pages")


class AnalyticsRequest(BaseModel):
    """Analytics request schema."""
    patient_ids: Optional[List[UUID]] = Field(None, description="Specific patient IDs")
    doctor_id: Optional[UUID] = Field(None, description="Filter by doctor")
    start_date: Optional[date] = Field(None, description="Start date for analytics")
    end_date: Optional[date] = Field(None, description="End date for analytics")
    metrics: List[str] = Field(
        default_factory=lambda: ["engagement", "adherence", "symptoms", "alerts"],
        description="Metrics to include"
    )


class PatientAnalytics(BaseModel):
    """Patient analytics data."""
    patient_id: UUID = Field(..., description="Patient ID")
    patient_name: str = Field(..., description="Patient name")
    treatment_type: Optional[str] = Field(None, description="Treatment type")
    current_day: int = Field(..., description="Current treatment day")
    
    # Engagement metrics
    total_messages_sent: int = Field(default=0, description="Total messages sent to patient")
    total_messages_received: int = Field(default=0, description="Total messages received from patient")
    response_rate: float = Field(default=0.0, description="Response rate percentage")
    avg_response_time_hours: Optional[float] = Field(None, description="Average response time in hours")
    
    # Quiz metrics
    quizzes_completed: int = Field(default=0, description="Number of completed quizzes")
    quiz_completion_rate: float = Field(default=0.0, description="Quiz completion rate percentage")
    avg_quiz_score: Optional[float] = Field(None, description="Average quiz score")
    
    # Alert metrics
    total_alerts: int = Field(default=0, description="Total alerts generated")
    high_priority_alerts: int = Field(default=0, description="High priority alerts")
    resolved_alerts: int = Field(default=0, description="Resolved alerts")
    
    # Trend data
    engagement_trend: List[dict[str, Any]] = Field(default_factory=list, description="Engagement trend data")
    symptom_trend: List[dict[str, Any]] = Field(default_factory=list, description="Symptom trend data")


