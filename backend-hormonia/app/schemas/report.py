class AnalyticsResponse(BaseModel):
    """Analytics response schema."""
    patient_analytics: List[PatientAnalytics] = Field(default_factory=list, description="Patient-specific analytics")
    system_analytics: SystemAnalytics = Field(..., description="System-wide analytics")
    generated_at: datetime = Field(default_factory=datetime.utcnow, description="Analytics generation timestamp")


class DashboardResponse(BaseModel):
    """Dashboard data response schema."""
    # Quick stats
    total_patients: int = Field(..., description="Total patients")
    active_patients: int = Field(..., description="Active patients")
    messages_today: int = Field(..., description="Messages sent today")
    alerts_pending: int = Field(..., description="Pending alerts")

    # Derived/summary metrics for frontend dashboard
    active_patients_percentage: float = Field(0.0, description="Percentage of active patients")
    response_rate: float = Field(0.0, description="Overall response rate percentage (last 7 days)")
    messages_sent: int = Field(0, description="Messages sent in the last 7 days")
    completed_quizzes: int = Field(0, description="Quizzes completed in the last 7 days")
    avg_response_time: float = Field(0.0, description="Average response time in minutes (approx)")

    # Trend data (percentage changes from previous period)
    patients_change: float = Field(0.0, description="Percentage change in total patients from previous period")
    active_patients_change: float = Field(0.0, description="Percentage change in active patients from previous period")
    messages_change: float = Field(0.0, description="Percentage change in messages from previous period")
    alerts_change: float = Field(0.0, description="Percentage change in alerts from previous period")
    response_rate_change: float = Field(0.0, description="Percentage change in response rate from previous period")
    quizzes_change: float = Field(0.0, description="Percentage change in completed quizzes from previous period")

    # Recent activity
    recent_messages: List[dict[str, Any]] = Field(default_factory=list, description="Recent messages")
    recent_alerts: List[dict[str, Any]] = Field(default_factory=list, description="Recent alerts")
    recent_quiz_completions: List[dict[str, Any]] = Field(default_factory=list, description="Recent quiz completions")

    # Charts data
    engagement_chart: List[dict[str, Any]] = Field(default_factory=list, description="Daily engagement series with messages_sent, responses_received, response_rate")
    alert_severity_chart: dict[str, Any] = Field(default_factory=dict, description="Alert severity distribution")
    treatment_progress_chart: dict[str, Any] = Field(default_factory=dict, description="Treatment progress chart")

    generated_at: datetime = Field(default_factory=datetime.utcnow, description="Dashboard generation timestamp")


