    quizzes_completed_week: int = Field(default=0, description="Quizzes completed this week")
    
    # Alert metrics
    alerts_generated_today: int = Field(default=0, description="Alerts generated today")
    unresolved_alerts: int = Field(default=0, description="Unresolved alerts")
    
    # Performance metrics
